// Episode 3

let murderer = 'Professor Plum';

const declareMurderer = function() {
  let murderer = 'Mrs. Peacock';
  return `The murderer is ${murderer}.`;
}

const firstVerdict = declareMurderer();
console.log('First Verdict: ', firstVerdict);

const secondVerdict = `The murderer is ${murderer}.`;
console.log('Second Verdict: ', secondVerdict);

// predicted = first verdict: Mrs. Peacock, second verdict: Professor Plum
// murderer in declare function is block scoped, as well as def scoped
// second verdict cannot access the murderer variable in declareMurderer


